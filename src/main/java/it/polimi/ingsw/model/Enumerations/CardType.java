package it.polimi.ingsw.model.Enumerations;

/**
 * The CarType enum represents the possible type of card in the game
 */
public enum CardType {
    /**
     * Plant Card (The green one)
     */
    PLANT,
    /**
     * Animal Card (The blue one)
     */
    ANIMAL,
    /**
     * Fungi Card (The red one)
     */
    FUNGI,
    /**
     * Insect Card (The purple one)
     */
    INSECT
}