package it.polimi.ingsw.model.Enumerations;

/**
 * The AnglePos Enum represents the possible positions where the angle can be
 */
public enum AnglePos {
    /**
     * Up Left position
     */
    UL,
    /**
     * Down Left position
     */
    DL,
    /**
     * Up Right position
     */
    UR,
    /**
     * Down Right position
     */
    DR
}